                <div class="textBody">   
                    <h3>Normas de conduta ajudam a evitar possíveis conflitos e problemas com vizinhos e funcionários</h2>  
                
                            
                    <h4>Pequenas atitudes no dia a dia influenciam a boa convivência e qualidade de vida de todos. Isso não foge à regra quando o assunto é relacionamento com os vizinhos. A harmonia e respeito entre condôminos e funcionários fazem a diferença na rotina do empreendimento, além de propiciar um ambiente mais saudável e leve.

                        A fim de orientar os moradores de edifícios residenciais sobre as boas práticas de convivência, a ItaBrasil - empresa com 46 anos de expertise em gestão e administração condominial - preparou um guia com pequenas regras básicas e dicas para evitar possíveis conflitos e problemas.
                    </h4>
                    <h3>Segurança</h2>
                    <h4>
                    - Evite facilitar a entrada de pessoas desconhecidas no condomínio sem a devida autorização. De maneira nenhuma abra o portão de entrada para pedestres ou veículos desconhecidos.
                    - Todos são responsáveis pela segurança do empreendimento, do condômino ao funcionário.
                    </h4>
                    <h3>Barulhos</h2>
                    <h4>
                    <p>- Não faça barulho após o horário de silêncio estabelecido e respeite as normas do condomínio.</p>
                    <p>- Mesmo no horário permitido, utilize o bom senso. Fique atento para não incomodar os vizinhos à sua volta com ruídos excessivos ou volume de som acima do normal.</p>
                    </h4>
                    <h3>Elevadores</h2>
                    <h4>
                    <p>- Seja cordial e cumprimente as pessoas ao entrar e sair do elevador.</p>
                    <p>- Espere que as pessoas saiam do elevador para que você possa entrar.</p>
                    <p>- Segurar a porta é um pequeno gesto de gentileza, que deve ser praticado sempre.</p>
                    <p>- Não monopolize e segure o elevador parado em um andar, especialmente em horários de pico.</p>
                    <p>- Animais, mudanças, caixas e compras de supermercado devem ser transportados sempre no elevador de serviço.</p>    
                    </h4>
                    <h3>Lixo</h2>
                    <h4>
                    <p>- Não jogue papéis, bitucas de cigarros ou quaisquer outros tipos de lixo pela janela da unidade.</p>
                    <p>- Seu lixo deve sempre estar devidamente ensacado, sem exalar odor forte.</p>
                    </h4>
                    <h3>Garagem e Estacionamento</h2>
                    <h4>
                    <p>- Estacione seu veículo da maneira correta na vaga. Respeite os limites das faixas com espaço para os demais carros e evite conflitos.</p>
                    <p>- Respeite a velocidade de 10 km/h na garagem.</p>
                    <p>- A preferência na circulação da garagem é sempre de quem está chegando.</p>
                    <p>- Não utilize sua vaga como depósito. Caso precise de um local para armazenar pertences, fale com seu zelador, gerente ou síndico do empreendimento.</p>
                    <p>- Em caso de raspadas e batidas de carros em outros veículos deixe um bilhete se identificando com pedido de desculpas para que o responsável escolha a melhor maneira de reparar os danos ou entre em contato com o proprietário do bem.
                    <p></p>- Sempre devolva o carrinho de compras ao local correto.
                    </h4>
                    <h3>Funcionários</h2>
                    <h4>
                    <p>- Trate todo e qualquer funcionário de forma cordial e educada, eles são pessoas valiosas para o dia a dia do condomínio.</p>
                    <p>- Em caso de reclamação, dirija sua queixa ao síndico ou gerente de empreendimento.</p>
                    <p>- Lembre-se que o funcionário é contratado para o condomínio, então evite pedir que ele execute serviços particulares.</p>
                    </h4>
                    <h3>Vizinhos</h2>
                    <h4>
                    <p>- Antes de reclamar de um vizinho, avalie se você não comete os mesmos equívocos.<p/>
                    <p>- Procure dialogar sempre de forma pacífica, com ponderação e bom senso.<p/>
                    <p>- Problemas e mal entendidos de condôminos devem ser resolvidos, preferencialmente, pessoalmente com uma conversa para compreender os dois lados da questão.<p/>
                    <p>- Um assunto somente deve ser levado ao síndico ou gerente do prédio somente em última instância.<p/>
                    </h4>
                    <h3>Crianças</h2>
                    <h4>
                    <p>- Ensine as regras do condomínio para os filhos e as crianças de sua unidade. É importante que aprendam a conviver em sociedade desde cedo, respeitando as normas e todas as pessoas igualmente.</p>
                    <p>- Menores de oito anos não devem circular desacompanhados, principalmente em áreas como a piscina.</p>
                    <p>-Procure limitar o horário de brincadeiras dentro do apartamento até às 22h para evitar conflitos com vizinhos por barulho.</p>
                    <p>- Opte por carpetes emborrachados e tapetes nas áreas onde as crianças ficam (geralmente nos quartos) para evitar propagação de ruídos.</p> 
                    </h4>
                    <h3>Regras e Assembleias</h2>                 
                    <h4>
                    <p></p>- Conheça a pauta antecipadamente e participe sempre que possível.</p>
                    <p></p>- Em caso de dúvidas, consulte sempre o síndico ou gerente do edifício.</p>
                    <p></p>- Lembre-se de que as regras de condomínio acordadas devem ser seguidas por todos, mesmo quem não estava presente.</p>
                    </h4>
                    <h3>Obras e reformas</h2>
                    <h4>
                    <p>- Consulte sempre o síndico ou gerente de empreendimento antes de realizar uma reforma para verificar se o reparo não entrará em conflito com as estruturas do empreendimento.</p>
                    <p>- Informe o que será realizado, bem como a duração, horários de ruídos e movimentação de prestadores de serviços.</p>
                    <p>- Comunique aos seus vizinhos e peça desculpas antes do início das obras.</p>
                    </h4>
                    <h3>Animais</h2>
                    <h4>
                    <p>- Animais devem ser transportados somente no elevador de serviço.</p>
                    <p>- Procure ficar atento ao barulho do animal para não perturbar os vizinhos, especialmente em horários de descanso.</p>
                    <p>- Caso o animal suja as dependências comuns do condomínio, o dono ou responsável deve limpar prontamente.</p>
                    </h4>
